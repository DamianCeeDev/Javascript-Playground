const day = 'Monday';

switch (day) {
    case 'Sunday':
        console.log("Day off. Chillin");
        break;
    case 'Monday':
        console.log('Morning Shift');
        break;
    case 'Tuesday':
        console.log('Day off');
        break;
    case 'Wednesday':
        console.log('Evening Shift');
        break;
    case 'Thursday':
        console.log('Evening Shift');
        break;
    case 'Friday':
        console.log('Morning Shift');
        break;
    case 'Saturday':
        console.log('Morning Shift');
        break;
    default:
        console.log('Invalid day');
}

